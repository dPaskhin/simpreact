// noinspection ES6UnusedImports
import * as SimpReact from '../../../../src/main/core';
import { EventBus } from '../../../../src/main/shared';
import * as SimpReactHooks from '../../../../src/main/hooks';

const gameManager = {
  eventBus: new EventBus(),

  currentMove: 0,
  history: [Array(9).fill(null)],

  get currentSquares() {
    return gameManager.history[gameManager.currentMove];
  },

  setMove(value) {
    gameManager.currentMove = value;
    gameManager.eventBus.publish();
  },

  setHistory(history) {
    gameManager.history = history;
    gameManager.eventBus.publish();
  },

  subscribe(listener) {
    return gameManager.eventBus.subscribe(listener);
  },
};

function Square({ value, onSquareClick }) {
  return (
    <button
      className="square"
      onClick={onSquareClick}
    >
      {value}
    </button>
  );
}

function Board({ xIsNext, squares, onPlay }) {
  function handleClick(i) {
    if (calculateWinner(squares) || squares[i]) {
      return;
    }
    const nextSquares = squares.slice();
    if (xIsNext) {
      nextSquares[i] = 'X';
    } else {
      nextSquares[i] = 'O';
    }
    onPlay(nextSquares);
  }

  const winner = calculateWinner(squares);
  let status;
  if (winner) {
    status = 'Winner: ' + winner;
  } else {
    status = 'Next player: ' + (xIsNext ? 'X' : 'O');
  }

  return (
    <>
      <div className="status">{status}</div>
      <div className="board-row">
        <Square
          value={squares[0]}
          onSquareClick={() => handleClick(0)}
        />
        <Square
          value={squares[1]}
          onSquareClick={() => handleClick(1)}
        />
        <Square
          value={squares[2]}
          onSquareClick={() => handleClick(2)}
        />
      </div>
      <div className="board-row">
        <Square
          value={squares[3]}
          onSquareClick={() => handleClick(3)}
        />
        <Square
          value={squares[4]}
          onSquareClick={() => handleClick(4)}
        />
        <Square
          value={squares[5]}
          onSquareClick={() => handleClick(5)}
        />
      </div>
      <div className="board-row">
        <Square
          value={squares[6]}
          onSquareClick={() => handleClick(6)}
        />
        <Square
          value={squares[7]}
          onSquareClick={() => handleClick(7)}
        />
        <Square
          value={squares[8]}
          onSquareClick={() => handleClick(8)}
        />
      </div>
    </>
  );
}

export function Game() {
  const rerender = SimpReactHooks.useRerender();

  const xIsNext = gameManager.currentMove % 2 === 0;

  SimpReactHooks.useEffect(() => {
    return gameManager.subscribe(rerender);
  }, []);

  function handlePlay(nextSquares) {
    gameManager.history.splice(gameManager.currentMove + 1);
    gameManager.history.push(nextSquares);

    gameManager.setHistory(gameManager.history);
    gameManager.setMove(gameManager.history.length - 1);
  }

  function jumpTo(nextMove) {
    gameManager.setMove(nextMove);
  }

  const moves = gameManager.history.map((squares, move) => {
    let description;
    if (move > 0) {
      description = 'Go to move #' + move;
    } else {
      description = 'Go to game start';
    }
    return (
      <li key={move}>
        <button onClick={() => jumpTo(move)}>{description}</button>
      </li>
    );
  });

  return (
    <div className="game">
      <div className="game-board">
        <Board
          xIsNext={xIsNext}
          squares={gameManager.currentSquares}
          onPlay={handlePlay}
        />
      </div>
      <div className="game-info">
        <ol>{moves}</ol>
      </div>
    </div>
  );
}

function calculateWinner(squares) {
  for (let i = 0; i < winningLines.length; i++) {
    const [a, b, c] = winningLines[i];
    if (squares[a] && squares[a] === squares[b] && squares[a] === squares[c]) {
      return squares[a];
    }
  }
  return null;
}

const winningLines = [
  [0, 1, 2],
  [3, 4, 5],
  [6, 7, 8],
  [0, 3, 6],
  [1, 4, 7],
  [2, 5, 8],
  [0, 4, 8],
  [2, 4, 6],
];
